#How to create an empty and a full NumPy array?
import numpy as np

# Creating an empty array with shape (2, 3)
empty_array = np.empty((2, 3))

print("Empty Array:")
print (empty_array)



# Creating a full array with shape (2, 3) filled with value 5
full_array = np.full((2, 3), 5)

print("\nFull Array:")
print(full_array)
